package engine.silnik;

    public class Vector2f {
        private float x;
        private float y;
        
        public Vector2f(float x, float y) {
            this.x = x;
            this.y = y;
        }
        
        public float getX() {return this.x;}
        public float getY() {return this.y;}
    }